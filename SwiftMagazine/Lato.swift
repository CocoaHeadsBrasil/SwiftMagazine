import UIKit

extension CocoaFonts {
    func latoBlackOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Black", size: size)
    }

    func latoBlackItalicOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-BlackItalic", size: size)
    }

    func latoBoldOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Bold", size: size)
    }

    func latoBoldItalicOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-BoldItalic", size: size)
    }

    func latoItalicOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Italic", size: size)
    }

    func latoRegularOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Regular", size: size)
    }
    
    func latoHairlineOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Hairline", size: size)
    }

    func latoHairlineItalicOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-HairlineItalic", size: size)
    }

    func latoLightOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-Light", size: size)
    }

    func latoLightItalicOfSize(size: CGFloat) -> UIFont {
        return UIFont(name: "Lato-LightItalic", size: size)
    }
}